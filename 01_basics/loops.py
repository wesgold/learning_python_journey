# While lopp
count = 1
while count <= 5:
    print(f"Count is : {count}")
    count += 1

# For loop
print("\nFor loop with range:")
for numbers in range(1, 6):
    print(f"Number: {numbers}")

# Looping through a list
languages = ["Python", "JavaScript", "SQL"]
for lang in languages:
    print(f"I want to learn {lang}")